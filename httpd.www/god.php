        <h4 class="textleft">THE BODY</h4>   
        <p class="textleft inleft">WE ARE DEVELOPING A CHOREOGRAPHY BASED ON FOOTAGE OF PEOPLE ENTERING INTO A TRANCE STATE BOTH FROM CLASSICAL ETHNOGRAPHIC FILMS AND FROM YOUTUBE TOGETHER WITH KARINA DICHOV LUND AND EMMA NORDANFORS FROM THE DANISH CHOREOGRAPHY COLLECTIVE E.K.K.O</p>
        <p class="textleft">When the clips are re-enacted by dancers Andrea Deres and Pontus Pettersson the documentary material is abstracted from its cultural and religious context. The movements that the ecstatic bodies perform during rituals in different religious contexts become abstract signs/dance and a tension is created between the uncontrolled power that comes from within the person who enters into trance and the control required when a professional dancer perform the same movements. DANSENS HUS, Feb 2013 DOCUMENTATION FROM WORKSHOP AT DANSENS HUS, STOCKHOLM, FEBRUARY. We held a six days workshop together with Karina Dichov Lund and Emma Nordanfors from the Danish choreograph collective E.K.K.O and dancers Andrea Deres and Pontus Petersson. The workshop was documented by cinematographer Erik Vallsten.</p>
        <p class="textleft">A note from the choreographers: “The aim of the workshop was to distinguish and to organize movements from filmed sequences of ecstasy from different cultures. We worked with copying the movements as exactly as possible, to get the timing right, the quality of the movements and how the movements are initiated. Looking at the clips over and over again together with the dancers we tried to reach a joint decision about what we are actually seeing people do in the clip. This was re-enacted by the dancers in the studio. We also put different material together to see how the clips contrasted with and influenced each other. Other try-outs of methods included instant copying as well as improvisation over single movements.”</p>       
        <h4 class="textleft">THE BRAIN</h4>
        <p class="textleft inleft">BY USING AN EXPERIMENTAL METHOD IN NEUROSCIENCE WE TRY TO LOCATE THE TURNING POINT OR LEAP IN THE BRAIN WHEN A PERSON ENTERS FROM A NORMAL CONSCIOUSNESS INTO AN ALTERED STATE.</p>
            <p class="textleft">We want to replace the film camera with a technical apparatus that cancels out the gap between passive viewing and direct experience of ecstasy. The origin is a study from the 1990s where Canadian neurologist Michael Persinger stimulated people’s brains with electromagnetism to provoke an altered state. The technical apparatus for this came to be called the “God helmet”. It was a conventional snowmobile helmet modified with solenoids and connected to a computer program that controlled the electromagnetic signals to put the subject into an altered state of consciousness. We held a 18 days workshop at Candyland ( Stockholm ), where we and twenty other participants used an experimental method in neuroscience known as the “God helmet” to reach an altered state. The sessions took place in total darkness in a soundproof booth in the gallery. Each session lasted for one hour and the participants did one, two or three sessions with the helmet. Exposed through the window of the gallery the enclosed scientific laboratory was opened up and displayed as sculptural object, installation and performance. The scientific experiment became a theater play at the same time as it was for real. The silver booth became a film camera and a movie theater when the participants projected their inner pictures and emotions in the darkness. We documented the workshop by placing a recorder in the booth directly after each session asking the participant to describe what had happened during the hour with the helmet. The workshop ended with a finissage where visitors could listen to the recordings from the workshop.</p>
